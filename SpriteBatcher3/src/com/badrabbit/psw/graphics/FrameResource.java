package com.badrabbit.psw.graphics;

public class FrameResource {

	public String name;
	public float x, y, width, height;
	
	public FrameResource(String name, float x, float y, float width, float height)
	{
		this.name = name;
		this.x = x;
		this.y = y;
		this.width = width;
		this.height = height;
	}
}
